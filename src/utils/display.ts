type Color =
  | 'black'
  | 'red'
  | 'green'
  | 'yellow'
  | 'blue'
  | 'magenta'
  | 'cyan'
  | 'white'
  | 'crimson'
  | 'grey'

type Util = 'reset' | 'bright' | 'dim' | 'underscore' | 'blink' | 'reverse'

interface Colors extends Record<Util, string> {
  fg: Record<Color, string>
  bg: Record<Color, string>
}

export const colors: Colors = {
  reset: '\x1b[0m',
  bright: '\x1b[1m',
  dim: '\x1b[2m',
  underscore: '\x1b[4m',
  blink: '\x1b[5m',
  reverse: '\x1b[7m',

  fg: {
    black: '\x1b[30m',
    red: '\x1b[31m',
    green: '\x1b[32m',
    yellow: '\x1b[33m',
    blue: '\x1b[34m',
    magenta: '\x1b[35m',
    cyan: '\x1b[36m',
    white: '\x1b[37m',
    grey: '\x1b[90m',
    crimson: '\x1b[38m', // Scarlet
  },
  bg: {
    black: '\x1b[40m',
    red: '\x1b[41m',
    green: '\x1b[42m',
    yellow: '\x1b[43m',
    blue: '\x1b[44m',
    magenta: '\x1b[45m',
    cyan: '\x1b[46m',
    white: '\x1b[47m',
    grey: '\x1b[49;5;8m',
    crimson: '\x1b[48m',
  },
}

export function color(str: string | number, ...args: string[]): string {
  return `${args.join('')}${str}${colors.reset}`
}

export async function sleep(ms) {
  return new Promise(resolve => {
    setTimeout(resolve, ms)
  })
}
